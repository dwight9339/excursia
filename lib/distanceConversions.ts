export const kmToMiles = (km: number) => Math.round(km * 0.621371);
export const metersToMiles = (meters: number) => Math.round(meters * 0.000621371);
export const milesToKm = (miles: number) => Math.round(miles * 1.60934);
export const milesToMeters = (miles: number) => Math.round(miles * 1609.34);
export const metersToKm = (meters: number) => Math.round(meters * 0.001);